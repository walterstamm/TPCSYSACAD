	ID_INSCRIPCION_CARRERA INT IDENTITY (1,1),
	ALUMNO INT NOT NULL FOREIGN KEY REFERENCES ALUMNOS(ID_ALUMNO),
	CARRERA VARCHAR(10) NOT NULL FOREIGN KEY REFERENCES CARRERAS(ID_CARRERA),
	AÑO INT NOT NULL DEFAULT (YEAR(GETDATE())),
	CUATRIMESTRE INT CHECK(CUATRIMESTRE = 1 OR CUATRIMESTRE = 2),
	ESTADO BIT NOT NULL DEFAULT(1)
)
