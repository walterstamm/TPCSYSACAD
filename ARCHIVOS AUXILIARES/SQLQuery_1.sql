	ID_DOCENTE INT PRIMARY KEY IDENTITY(1,1),
	CUIL VARCHAR(14) NOT NULL UNIQUE,
	APELLIDO_NOMBRE VARCHAR(50) NOT NULL,
	NACIONALIDAD VARCHAR(30) NOT NULL,
	FECHA_NAC DATE NOT NULL CHECK(FECHA_NAC < GETDATE()),
	MAIL VARCHAR (30) NOT NULL,
	DOMICILIO VARCHAR(50),
	LOCALIDAD_PCIA VARCHAR(50),
	USUARIO VARCHAR(20) DEFAULT(NULL),
	ESTADO INT NOT NULL DEFAULT(0) CHECK(ESTADO = 0 OR ESTADO = 1 OR ESTADO = 2)
)
GO

CREATE TABLE CARRERAS(
	ID_CARRERA VARCHAR(10) PRIMARY KEY,
	NOMBRE_CARRERA VARCHAR(100) NOT NULL UNIQUE,
	PLAN_AÑO VARCHAR(5) NOT NULL,
	DOC_HABILITANTE VARCHAR(50) NOT NULL, --RESOLUCION QUE HABILITE LA CARRERA
	ESTADO BIT DEFAULT(1)
)
GO

CREATE TABLE MATERIAS(
	ID_MATERIA VARCHAR(10) PRIMARY KEY,
	NOMBRE_MATERIA VARCHAR(50) NOT NULL,
	CARGA_HORARIA_MINIMA INT NOT NULL CHECK(CARGA_HORARIA_MINIMA > 0),
	CARRERA VARCHAR(10) NOT NULL FOREIGN KEY REFERENCES CARRERAS(ID_CARRERA),
	AÑO_CURSO INT NOT NULL CHECK(AÑO_CURSO = 1 OR AÑO_CURSO = 2),
	CUATRIMESTRE INT NOT NULL CHECK(CUATRIMESTRE = 1 OR CUATRIMESTRE = 2),
	ESTADO BIT NOT NULL DEFAULT(1)
)
GO

